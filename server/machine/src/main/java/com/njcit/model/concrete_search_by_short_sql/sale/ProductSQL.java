public class ProductSQL {

    private String product_id;
    private String product_name;
    private String supplier_id;
    private String type_id;
    private String per_count;
    private String price;
    private String storage_count;

    public String getProduct_id() {
        return product_id;
    }

    public void setProduct_id(String product_id) {
        this.product_id = product_id;
    }

    public String getProduct_name() {
        return product_name;
    }

    public void setProduct_name(String product_name) {
        this.product_name = product_name;
    }

    public String getSupplier_id() {
        return supplier_id;
    }

    public void setSupplier_id(String supplier_id) {
        this.supplier_id = supplier_id;
    }

    public String getType_id() {
        return type_id;
    }

    public void setType_id(String type_id) {
        this.type_id = type_id;
    }

    public String getPer_count() {
        return per_count;
    }

    public void setPer_count(String per_count) {
        this.per_count = per_count;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getStorage_count() {
        return storage_count;
    }

    public void setStorage_count(String storage_count) {
        this.storage_count = storage_count;
    }




}
